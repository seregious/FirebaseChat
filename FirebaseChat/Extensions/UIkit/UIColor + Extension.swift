    }
    
    static func mainWhite() -> UIColor {
        UIColor(red: 245/255, green: 245/255, blue: 250/255, alpha: 1)
    }
    
    static func buttonBlack() -> UIColor {
        UIColor(red: 50/255, green: 50/255, blue: 60/255, alpha: 1)
    }
    
    static func textFieldGrey() -> UIColor {
        UIColor(red: 100/255, green: 100/255, blue: 100/255, alpha: 1)
    }
}
